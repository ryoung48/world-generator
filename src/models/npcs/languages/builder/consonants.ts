const orthography = (): Record<string, string> => {
  return {
    ŋ: 'ng',
    ð: 'th',
    ʃ: window.dice.weightedChoice([
      { v: 'sh', w: 0.9 },
      { v: 'x', w: 0.1 }
    ]),
    ʧ: window.dice.weightedChoice([
      { v: 'ch', w: 0.9 },
      { v: 'q', w: 0.1 }
    ]),
    ʒ: 'zh'
  }
}

const mapOrtho = (ortho: Record<string, string>, sounds: string[]) =>
  sounds.map(c =>
    c
      .split('')
      .map(l => ortho[l] || l)
      .join('')
  )

